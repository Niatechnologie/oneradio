    <p class="lead">Cette politique explique comment l'application mobile ONE RADIO traite les informations des utilisateurs. En résumé : l'application diffuse uniquement un flux radio en direct et ne collecte aucune donnée personnelle.</p>

    <section>
      <h2>1. Données collectées</h2>
      <div class="callout">
        <p>ONE RADIO ne collecte, ne stocke et ne transmet aucune donnée personnelle identifiable. L'application n'exige aucune inscription, aucun compte utilisateur et ne demande aucune information personnelle (nom, e-mail, numéro de téléphone, localisation, etc.) pour fonctionner.</p>
        <p>L'unique fonction de l'application est la diffusion en direct du flux audio de la radio.</p>
      </div>
    </section>

    <section>
      <h2>2. Permissions utilisées</h2>
      <p>Pour assurer la diffusion du flux audio, l'application nécessite uniquement un accès à Internet et à l'état de la connexion réseau de l'appareil. Cette permission sert exclusivement à charger et lire le flux radio en direct ; elle n'est utilisée pour aucune autre finalité.</p>
    </section>

    <section>
      <h2>3. Publicités et trackers</h2>
      <p>L'application n'affiche aucune publicité et n'intègre aucun service publicitaire (AdMob ou autre). Elle ne contient aucun outil de suivi publicitaire ou comportemental.</p>
    </section>

    <section>
      <h2>4. Partage des données avec des tiers</h2>
      <p>Aucune donnée n'étant collectée, aucune donnée n'est partagée, vendue ou transmise à des tiers, qu'il s'agisse d'annonceurs, d'analystes ou de tout autre partenaire commercial.</p>
    </section>

    <section>
      <h2>5. Services tiers</h2>
      <p>L'application se connecte uniquement au serveur de diffusion du flux radio de ONE RADIO pour permettre l'écoute en direct. Aucun autre service tiers (analytics, publicité, réseaux sociaux) n'est intégré à l'application.</p>
    </section>

    <section>
      <h2>6. Sécurité</h2>
      <p>Dans la mesure où l'application ne collecte ni ne stocke de données personnelles, aucun risque lié à la sécurité de données utilisateur n'est applicable dans le cadre de son fonctionnement actuel.</p>
    </section>

    <section>
      <h2>7. Confidentialité des enfants</h2>
      <p>L'application ne collecte aucune information, y compris auprès des enfants de moins de 13 ans. Elle peut être utilisée par tout public sans risque lié à la collecte de données personnelles.</p>
    </section>

    <section>
      <h2>8. Modifications de cette politique</h2>
      <p>Cette politique de confidentialité peut être mise à jour si de nouvelles fonctionnalités venaient à être ajoutées à l'application (par exemple, des notifications ou des statistiques d'usage). Toute modification substantielle sera reflétée sur cette page avec une nouvelle date de mise à jour.</p>
    </section>

